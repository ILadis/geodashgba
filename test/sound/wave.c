test(FillSample_ShouldReturnExpectedSamplesAndWrapAround) {
  // arrange
  DataSource *source = Buffer_From(&(Buffer) {0}, wave8, length(wave8));
  Reader *reader = DataSource_AsReader(source);

  SoundSampler *sampler = WaveSoundSampler_From(&(WaveSoundSampler) {0}, reader);

  char samples[] = {
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x9d, 0xa3, 0xa9, 0xaf, 0xb5, 0xbb, 0xc1, 0xc7,
    0xcd, 0xd2, 0xd8, 0xde, 0xe4, 0xea, 0xf0, 0xf6,
  };

  int buffer[64] = {0};
  unsigned int position = 0;
  unsigned int increment = 1 << SOUND_CHANNEL_PRECISION;
  unsigned char volume = 1 << SOUND_VOLUME_PRECISION;

  // act
  int *next = SoundSampler_FillBuffer(sampler, buffer, &position, increment, volume, length(buffer));

  // assert
  for (unsigned int i = 0; i < 32; i++) {
    assert(buffer[i]      == samples[i]);
    assert(buffer[i + 32] == samples[i]);
  }
  assert(next == buffer + 64);
}

